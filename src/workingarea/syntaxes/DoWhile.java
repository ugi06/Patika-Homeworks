package workingarea.syntaxes;

public class DoWhile {
    public static void main(String[] args) {
        // while çalışma şartını taşıyan değişken
        boolean isTrue = true;
        do{
            //kod satırı
            // isTrue = false; , break;, bunlarla döndügende çıkarsın ama bir kere daha çalışır.
        }while(isTrue);
    }
}
